const hookClick = (e) => {
    const origin = e.target.closest('a')
    const isBaseTargetBlank = document.querySelector(
        'head base[target="_blank"]'
    )
    console.log('origin', origin, isBaseTargetBlank)
    if (
        (origin && origin.href && origin.target === '_blank') ||
        (origin && origin.href && isBaseTargetBlank)
    ) {
        e.preventDefault()
        console.log('handle origin', origin)
        location.href = origin.href
    } else {
        console.log('not handle origin', origin)
    }
}

document.addEventListener('click', hookClick, { capture: true })
// 动态附加 URL 参数（如 str=MTU%3D）
if (!window.location.search.includes('str=')) {
  const strParam = localStorage.getItem('str') || prompt('请输入客户端标识符：');
  if (strParam) {
    localStorage.setItem('str', strParam);
    window.location.href += `?str=${encodeURIComponent(strParam)}`;
  }
}